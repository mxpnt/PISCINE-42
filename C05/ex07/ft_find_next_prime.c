int	ft_is_prime(int nb)
{
	int	i;

	i = 3;
	while (i < nb / 2 && i < 46341)
	{
		if (nb % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	if (nb <= 2)
		return (2);
	if (nb % 2 == 0)
		nb++;
	if (ft_is_prime(nb))
		return (nb);
	while (!ft_is_prime(nb))
		nb += 2;
	return (nb);
}
